  const body =  new Blog(request.body)

  if(!body.title && !body.url) {
    response.status(400)
    response.send()
    return
  }

  const blog = new Blog({
    title: body.title,
    author: body.author,
    url: body.url,
    likes: body.likes === undefined ? 0 : body.likes
  })

